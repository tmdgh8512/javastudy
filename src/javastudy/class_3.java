package javastudy;
//변수
public class class_3 {

	public static void main(String[] args) {

		int i = 10;
		int j;
		j = 100;
		i = 200; // i값 변경(10 --> 200)

		System.out.println("i + j = " + (i + j));

		int num = 10;
		System.out.println("num = " + num);
		num = 25;
		System.out.println("num= " + num);
		num = 100;
		System.out.println("num = " + num);

		// 변수에 저장된 데이터는 언제든지 변경할 수 있다.
	}

}
